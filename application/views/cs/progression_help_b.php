    <h4 class="text-success">Milestone moderation</h4>
    There are 3 people/groups involved in the milestone review and approval process:
    <ol>
        <li>Student</li>
        <li>Postgraduate coordinator</li>
        <li>College RHD Committee (via the RHD Admin Officer)</li>
    </ol>
    Each person is notified by email (from REX) when their response is required.
    The email contains the direct link to the milestone to review in the online workflow.
